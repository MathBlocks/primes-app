const generatePackedAttributes = (): PackedAttributes => {
  const entries = Object.keys(packedAttributeNames).map(
    (id, attrIdx) => {
      const members = PACKED.reduce<number[]>(
        (prev, packedValue, n) =>
          ((packedValue >> attrIdx) & 1) === 1 ? [...prev, n] : prev,
        [],
      )
      return [id, new Set<number>(members)]
    },
  )

  return Object.fromEntries(entries)
}

const generateCoreAttributes = () => {
  return {
    unit: new Set([1]),
    prime: new Set(NUMBERS.filter((n) => n > 0 && isPrime(n))),
    composite: new Set(NUMBERS.filter((n) => n > 2 && !isPrime(n))),
  }
}

